package day02;

public class PrimitiveType {
	public static void main(String[] args)
	{
		
		System.out.println("-----기본자료형------");
		int a=10;
		int b=010;
		int c=0x2ac;
		int d = 0x12;
		System.out.println("a="+a);
		System.out.println("b="+b);
		System.out.println("c="+c);
		System.out.println("d="+d);
		double d2 = .456E+2;
		System.out.println(d2);
	}
}
